package zad9b;

import zad9a.Point;

public class PointController {

    public void addX(Point point) {
        point.x++;
    }

    public void minusX(Point point) {
        point.x--;
    }

    public void addY(Point point) {
        point.y++;
    }

    public void minusY(Point point) {
        point.y--;
    }

}
